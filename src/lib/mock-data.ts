// TODO: swap mock data for real DB call — this in-memory upsert stands in for
// `prisma.attempt.upsert()` until Postgres is wired up.
export function upsertMockAttempt(
  payload: Omit<Attempt, 'id' | 'createdAt' | 'updatedAt'>,
): Attempt {
  const now = new Date().toISOString()
  const existingIndex = mockAttempts.findIndex(
    (a) => a.userId === payload.userId,
  )

  if (existingIndex !== -1) {
    const updated: Attempt = {
      ...mockAttempts[existingIndex],
      ...payload,
      updatedAt: now,
    }
    mockAttempts[existingIndex] = updated
    return updated
  }

  const created: Attempt = {
    ...payload,
    id: `att_${Math.random().toString(36).slice(2, 10)}`,
    createdAt: now,
    updatedAt: now,
  }
  mockAttempts.push(created)
  return created
}
